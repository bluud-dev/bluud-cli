#!/usr/bin/env node
/**
 * CI gate: confirm `dist/skill/SKILL.md`'s stamped `metadata.version`
 * (written by `tsup.config.ts`'s `onSuccess`, via `src/lib/skillVersion.ts`)
 * matches `package.json`'s version.
 *
 * Run after `npm run build`. Exits non-zero — failing the `pack` CI job —
 * when the two disagree, which can only happen if the build step's stamping
 * silently regressed, since both are derived from the same `package.json`
 * read at build time.
 *
 * Reimplements the frontmatter delimiter regex rather than importing
 * `src/lib/skillVersion.ts` directly: this script runs as plain Node against
 * the built `dist/` output, outside tsup's TypeScript-aware config loader,
 * so it cannot import a `.ts` module without a loader. See that file's header
 * comment — this is the fourth deliberate copy of the same stable pattern.
 */
import { readFileSync } from "node:fs";

const FRONTMATTER_PATTERN = /^---\r?\n([\s\S]*?)\r?\n---\r?\n?([\s\S]*)$/;

const pkg = JSON.parse(readFileSync("package.json", "utf8"));
const skillMd = readFileSync("dist/skill/SKILL.md", "utf8");

const match = skillMd.match(FRONTMATTER_PATTERN);
if (!match) {
  console.error("::error::dist/skill/SKILL.md has no parseable YAML frontmatter.");
  process.exit(1);
}

// The version line lives nested (indented) under `metadata:` — a simple
// indented-key match is sufficient here since the frontmatter is
// machine-generated by the build step, not hand-authored.
const stampedLine = match[1].split(/\r?\n/).find((line) => /^\s+version:\s*/.test(line));
const stamped = stampedLine ? stampedLine.replace(/^\s+version:\s*/, "").trim() : null;

console.log(`package.json version:            ${pkg.version}`);
console.log(`dist/skill/SKILL.md metadata.version: ${stamped ?? "(missing)"}`);

if (stamped !== pkg.version) {
  console.error(
    `::error::Bundled skill version (${stamped ?? "missing"}) does not match package.json version (${pkg.version})`,
  );
  process.exit(1);
}

console.log("Skill version pin verified.");
